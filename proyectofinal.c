//esta parte es para dar la posicion del caballero, si el caballero se encuentra en esa posicion aparece un * en la posicion i,j
void printCaballero(int posi, int posj) {
    
    for (int i = 0; i < 5; i++) {
	    
            for (int j = 0; j < 5; j++) {
            
                    if(posi==i && posj==j){
			    
                            printf("[*]");
                    }
                    else{
			    
                            printf("[]");
                    }
            }
            printf("\n"); 
    }
}

